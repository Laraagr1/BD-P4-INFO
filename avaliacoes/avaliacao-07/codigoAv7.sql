CREATE TABLE TB_CLIENTES (
  COD_CLI INTEGER NOT NULL,
  NOME_CLI TEXT NOT NULL,
  END_CLI TEXT NOT NULL, 
  PRIMARY KEY (COD_CLI AUTOINCREMENT)
);

CREATE TABLE TB_VENDEDORES (
  ID INTEGER NOT NULL, 
  NOME TEXT NOT NULL, 
  PRIMARY KEY (ID AUTOINCREMENT)
);
  
CREATE TABLE TB_NOTAS_FISCAIS (
  NUM_NF INTEGER NOT NULL,
  SERIE_NF INTEGER NOT NULL,
  COD_CLI INTEGER NOT NULL,
  COD_VEND INTEGER NOT NULL, 
  PRIMARY KEY (NUM_NF AUTOINCREMENT),
  FOREIGN KEY (COD_CLI) REFERENCES TB_CLIENTES(COD_CLI),
  FOREIGN KEY (COD_VEND) REFERENCES TB_VENDEDORES(ID)
);

CREATE TABLE TB_PRODUTOS (
  ID INTEGER NOT NULL,
  COD_PRO INTEGER NOT NULL,
  NOME_PROD TEXT NOT NULL,
  PRECO_UNIT REAL NOT NULL,
  PRIMARY KEY(ID AUTOINCREMENT)
);

CREATE TABLE TB_ITENS_NOTAS_FISCAIS (
  VALOR INTEGER, 
  QTD INTEGER NOT NULL, 
  COD_PRO INTEGER NOT NULL, 
  NUM_NF INTEGER NOT NULL,
  PRIMARY KEY (COD_PRO, NUM_NF), 
  FOREIGN KEY (COD_PRO) REFERENCES TB_PRODUTOS(ID)
);

